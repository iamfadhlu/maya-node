def get_alias(chain, addr):
    if chain == "BNB":
        aliases = aliases_bnb
    if chain == "BTC":
        aliases = aliases_btc
    if chain == "DASH":
        aliases = aliases_dash
    if chain == "ETH":
        aliases = aliases_eth
    if chain == "ARB":
        aliases = aliases_arb
    if chain == "KUJI":
        aliases = aliases_kuji
    if chain == "MAYA":
        aliases = aliases_maya
    if chain == "THOR":
        aliases = aliases_thor
    if chain == "XRD":
        aliases = aliases_xrd
    for name, alias_addr in aliases.items():
        if alias_addr == addr:
            return name
